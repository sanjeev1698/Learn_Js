//  Day 1 --> 3 types of  Variables : var,let,const.

// var can redeclare and initilize the value
var a = 1;
console.log(a);

var a = 2;
console.log(a);

// let --> can't redeclare and initilize the value
let b = 2 ;
console.log(b);
// const --> constant value
const c = 4;
console.log(c);
